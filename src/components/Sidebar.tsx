export default function Sidebar({ license }: Props) {
  const planLabel = license.plan.charAt(0).toUpperCase() + license.plan.slice(1);

  return (
    <aside className="w-56 bg-gray-900 border-r border-gray-800 flex flex-col">
      {/* Logo */}
      <div className="px-4 py-5 border-b border-gray-800">
        <h1 className="text-lg font-semibold text-white">Forge</h1>
        <p className="text-xs text-gray-500 mt-0.5">Plugin Manager</p>
      </div>

      {/* Navigation */}
      <nav className="flex-1 px-3 py-4 space-y-1">
        {navItems.map(({ to, label, icon: Icon }) => (
          <NavLink
            key={to}
            to={to}
            className={({ isActive }) =>
              `flex items-center gap-3 px-3 py-2 rounded-lg text-sm transition-colors ${
                isActive
                  ? 'bg-forge-600/20 text-forge-300'
                  : 'text-gray-400 hover:text-gray-200 hover:bg-gray-800'
              }`
            }
          >
            <Icon />
            {label}
          </NavLink>
        ))}
      </nav>

      {/* License info */}
      <div className="px-4 py-3 border-t border-gray-800">
        <div className="flex items-center gap-2">
          <div className={`w-2 h-2 rounded-full ${license.is_active ? 'bg-green-500' : 'bg-red-500'}`} />
          <span className="text-xs text-gray-400">{planLabel} Plan</span>
        </div>
        <p className="text-[10px] text-gray-600 mt-1 font-mono">
          {license.license_key.slice(0, 14)}...
        </p>
      </div>
    </aside>
  );
}

